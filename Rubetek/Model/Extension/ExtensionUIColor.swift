import UIKit

extension UIColor {
    
    static let lightGrayBackground: UIColor = {
        return UIColor(red: 246/255, green: 246/255, blue: 246/255, alpha: 1)
    }()
    
    static let blueApplicatonColor: UIColor = {
        return UIColor(red: 80/255, green: 166/255, blue: 238/255, alpha: 1)
    }()
    
}
